package variable.ex;

public class VarEx2Question3 {
    public static void main(String[] args) {
        // long 타입의 변수를 선언하고, 그 변수를 100,000,000,000(백억)으로 초기화 후 출력
        // boolean 타입의 변수를 선언하고, 그 변수를 true로 초기화 후 출력

        long longVar = 100000000000L;
        boolean boolVar = true;

        System.out.println(longVar);
        System.out.println(boolVar);
    }
}
